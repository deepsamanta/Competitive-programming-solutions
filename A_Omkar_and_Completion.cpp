#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
int main()
{
    ll test;
    cin >> test;
    while (test--)
    {
        ll a;
        cin >> a;
        for (int i = 1; i <= a; i++)
        {
            cout << 1 << " ";
        }
        cout << endl;
    }
}